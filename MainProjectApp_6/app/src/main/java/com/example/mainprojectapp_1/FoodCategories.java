package com.example.mainprojectapp_1;

public class FoodCategories {
    private int image;
    private String name;

    public FoodCategories(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

}
